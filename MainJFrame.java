    private void jButtonDeteleStrategyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeteleStrategyActionPerformed
        this.classList.remove(this.jComboBoxStrategy.getSelectedIndex());
        this.strategyMap.remove(this.jComboBoxStrategy.getSelectedItem().toString());
        this.jComboBoxStrategy.removeItemAt(this.jComboBoxStrategy.getSelectedIndex());

        if (this.jComboBoxStrategy.getItemCount() < 1) {
            this.jButtonDeteleStrategy.setEnabled(false);
            this.jButtonEditStrategy.setEnabled(false);
            this.jTextAreaLookStrategyArea.setText("");
        } else if (this.jComboBoxStrategy.getItemCount() < 2) {
            this.jButtonStartWar.setEnabled(false);
        } else {
            this.jTextAreaLookStrategyArea.setText(this.strategyMap.get(this.jComboBoxStrategy.getSelectedItem().toString()));
        }
    }//GEN-LAST:event_jButtonDeteleStrategyActionPerformed

