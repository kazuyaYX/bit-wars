    private void jButtonStartWarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStartWarActionPerformed
        if ("".equals(jTextRound.getText())) {
            JOptionPane.showMessageDialog(this, "请输入要对决的回合数！");
            return;
        }
        int round = Integer.valueOf(jTextRound.getText());
        int[][] scoreTable = {{1, 5}, {0, 3}};
        if (round < 1) {
            JOptionPane.showMessageDialog(this, "回合数不能小于1");
            return;
        }
        strategyList.clear();
        for (int i = 0; i < jComboBoxStrategy.getItemCount(); i++) {
            strategyList.add(new Strategy(jComboBoxStrategy.getItemAt(i).toString(), 0));
        }

        //开始比赛
        for (int i = 0; i < classList.size(); i++) {
            Integer[] A = new Integer[round + 1];
            Integer[] B = new Integer[round + 1];
            for (int j = i + 1; j < classList.size(); j++) {
                try {
                    for (int currentRound = 1; currentRound <= round; currentRound++) {
                        A[currentRound] = (int) classList.get(i).getClass().getMethod("decision", Integer.class,
                                Integer[].class, Integer[].class).invoke(classList.get(i), currentRound, A, B);
                        B[currentRound] = (int) classList.get(j).getClass().getMethod("decision", Integer.class,
                                Integer[].class, Integer[].class).invoke(classList.get(j), currentRound, B, A);
                        strategyList.get(i).addScore(scoreTable[A[currentRound]][B[currentRound]]);
                        strategyList.get(j).addScore(scoreTable[B[currentRound]][A[currentRound]]);
                    }
                } catch (Exception ex) {
                    Logger.getLogger(MainJFrame.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, "大战出错，请重启程序！");
                    return;
                }
            }
        }

        Collections.sort(strategyList);
        jTextAreaResultArea.setText("各策略两两对战" + round + "轮\n各策略得分如下：\n");
        for (int i = 0; i < strategyList.size(); i++) {
            jTextAreaResultArea.append(String.valueOf(i+1) + ". " + strategyList.get(i).getName()
                    + "  得分： " + String.valueOf(strategyList.get(i).getScore())
                    + "\n");
        }

    }//GEN-LAST:event_jButtonStartWarActionPerformed

